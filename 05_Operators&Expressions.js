console.log("Operators in js")
let a = 10;
let b = 5;
console.log("a + b = ", a + b);
console.log("a - b = ", a - b);
console.log("a * b = ", a * b);
console.log("a ** b = ", a ** b);
console.log("a / b = ", a / b);
console.log("a % b = ", a % b);
console.log("a++ = ", a++);
console.log("++a = ", ++a);
console.log("a-- = ", a--);
console.log("--a = ", --a);
console.log("a = ", a);

